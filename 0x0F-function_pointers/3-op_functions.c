/**
 * op_add - adds two integers
 * @a: arg 1
 * @b: arg 2
 * Return: sum of integers
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtracts a from b
 * @a: first arg
 * @b: second arg
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies two integers
 * @a: first arg
 * @b: second arg
 * Return: product of two args
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides two integers
 * @a: arg 1
 * @b: arg 2
 * Return: division of integers
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - gets the modulus of two args
 * @a: arg 1
 * @b: arg 2
 * Return: modulus(a % b)
*/
int op_mod(int a, int b)
{
	return (a % b);
}
